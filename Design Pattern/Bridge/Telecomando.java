package Bridge;

abstract class Telecomando { //Abstraction
	
	protected Device device;

    public Telecomando(Device device) {
        this.device = device;
    }

    public void turnOn() {
        device.turnOn();
    }

    public void turnOff() {
        device.turnOff();
    }

    public void setVolume(int volume) {
        device.setVolume(volume);
    }
}
